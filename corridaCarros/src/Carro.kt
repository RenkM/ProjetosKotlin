// classe que representa um carro
class Carro (

    val modelo: String,        // nome do carro
    val velocidade: Int,       // velocidade máxima
    val aceleracao: Int,       // rapidez pra ganhar velocidade
    val freio: Int,            // eficiência de frenagem
    val tanque: Int,           // capacidade de combustível
    val consumo: Int,          // quanto consome por volta
    val resistencia: Int,      // "vida" do carro
    val piloto: Piloto         // piloto que dirige o carro

)
